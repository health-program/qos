package com.paladin.qos.service.org.vo;

public class OrgPersonReport {

    private Long healthPeopleNumber;

    private Long nonHealthPeopleNumber;

    public Long getHealthPeopleNumber() {
        return healthPeopleNumber;
    }

    public void setHealthPeopleNumber(Long healthPeopleNumber) {
        this.healthPeopleNumber = healthPeopleNumber;
    }

    public Long getNonHealthPeopleNumber() {
        return nonHealthPeopleNumber;
    }

    public void setNonHealthPeopleNumber(Long nonHealthPeopleNumber) {
        this.nonHealthPeopleNumber = nonHealthPeopleNumber;
    }
}
